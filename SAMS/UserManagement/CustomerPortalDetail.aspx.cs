    protected void lbCustomerName_Click(object sender, EventArgs e)
    {
        var lbCustomerName = (LinkButton)sender;
        var row = (GridViewRow)lbCustomerName.NamingContainer;
        var hfportalLoginGuid = (HiddenField)gvUserDetails.Rows[row.RowIndex].FindControl("hfportalLoginGuid");
       // const string pageStatus = "C";
        Response.Redirect("../UserManagement/CreatePortalUser.aspx?PortalLoginGuid=" + hfportalLoginGuid.Value);
    }
