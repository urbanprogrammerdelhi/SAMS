    /// <summary>
    /// Handles the Load event of the Page control.
    /// </summary>
    /// <param name="sender">The source of the event.</param>
    /// <param name="e">The <see cref="EventArgs"/> instance containing the event data.</param>
    protected void Page_Load(object sender, EventArgs e)
    {
        if (!IsPostBack)
        {
            if (IsReadAccess)
            {
                var javaScript = new System.Text.StringBuilder();
                javaScript.Append("<script type='text/javascript'>");
                javaScript.Append("window.document.body.onload = function ()");
                javaScript.Append("{\n");
                javaScript.Append("PageTitle('" + Resources.Resource.UserDetail + "');");
                javaScript.Append("};\n");
                javaScript.Append("// -->\n");
                javaScript.Append("</script>");
                this.Page.ClientScript.RegisterClientScriptBlock(this.GetType(), "BodyLoadUnloadScript", javaScript.ToString());

                FillgvUserDetails();
            }
            else
            {
                Response.Redirect("../UserManagement/Home.aspx");
            }
        }
    }

    /// <summary>
    /// Fillgvs the user details.
    /// </summary>
    private void FillgvUserDetails()
    {
        var objPortal = new BL.Portal();
        var dt = new DataTable();
        int dtflag = 1;
        dt = objPortal.PortalUserDetailGet(BaseCompanyCode,"").Tables[0];
        if (dt != null)
        {
            //to fix empety gridview
            if (dt.Rows.Count == 0)
            {
                dtflag = 0;
                dt.Rows.Add(dt.NewRow());
            }
            gvUserDetails.DataSource = dt;
            gvUserDetails.DataBind();

            if (dtflag == 0) //to fix empety gridview
            {
                gvUserDetails.Rows[0].Visible = false;
            }
        }

    }

    /// <summary>
    /// Handles the Click event of the lbCustomerName control.
